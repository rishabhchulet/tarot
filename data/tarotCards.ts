// Combine all cards for the complete deck
export const TAROT_CARDS = [...MAJOR_ARCANA, ...MINOR_ARCANA];

// Export individual suits for easier access
export const WANDS = MINOR_ARCANA.filter(card => card.suit === 'Wands');
export const CUPS = MINOR_ARCANA.filter(card => card.suit === 'Cups');
export const SWORDS = MINOR_ARCANA.filter(card => card.suit === 'Swords');
export const PENTACLES = MINOR_ARCANA.filter(card => card.suit === 'Pentacles');

// Helper function to get a random card from a specific suit
export const getRandomCardFromSuit = (suit: 'Major Arcana' | 'Wands' | 'Cups' | 'Swords' | 'Pentacles') => {
  const suitCards = TAROT_CARDS.filter(card => card.suit === suit);
  return suitCards[Math.floor(Math.random() * suitCards.length)];
};

// Helper function to get a random card from the entire deck
export const getRandomCard = () => {
  return TAROT_CARDS[Math.floor(Math.random() * TAROT_CARDS.length)];
};